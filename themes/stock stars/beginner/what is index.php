    <p><b class="topic">What is an index?</b><br><br>
    An index is a market benchmark that tracks the performance of a basket of securities. This basket of securities represents the entire market. An index is made up of securities that are chosen based on market capitalisation or other criteria. Any change in the price of the underlying securities affects the value of the index. Since the index represents the entire market, any change in the value of the index affects the value of companies that are not listed and other financial products, including commodities.<br><br>

Indexes are also created to measure economic indicators like inflation and interest rates. They act as a benchmark to gauge portfolio returns. Investors can compare their investment portfolio returns against the benchmark returns and make changes to their portfolio if needed. The popular indexes in the country are Nifty 50 and BSE Sensex.
